<template>
  <div>
    <label
      v-if="props.title"
      :for="props.id"
      class="block text-sm font-medium"
      :class="titleClasses"
    >
      {{ props.title }}
      <span
        v-if="required && !formSettings.hideRequiredLabel"
        :class="formSettings.requiredLabelClasses"
        >{{ formSettings.requiredLabel }}</span
      >
    </label>

    <div class="mt-1 w-full relative">
      <textarea
        v-if="props.textArea"
        :id="id"
        :type="type"
        :aria-required="required"
        :placeholder="placeholder"
        :value="modelValue"
        :data-test="id"
        class="appearance-none block w-full px-3 py-2 border rounded-sm shadow-sm focus:outline-none sm:text-sm"
        :class="textBoxClasses"
        @input="valueChanged"
      />
      <input
        v-else
        :id="props.id"
        v-model="inputValue"
        :data-test="props.id"
        :placeholder="props.placeholder"
        :type="type"
        :name="props.id"
        :autocomplete="props.id"
        :aria-invalid="inError"
        :disabled="props.disabled"
        required
        class="appearance-none block w-full px-3 py-2 border rounded-sm shadow-sm focus:outline-none sm:text-sm"
        :class="textBoxClasses"
        v-bind="$attrs"
        :passwordrules="
          password
            ? `minlength: ${minPasswordLength}; maxlength: ${maxPasswordLength}; required: lower; required: upper; required: digit; required: special;`
            : undefined
        "
        :minlength="password ? minPasswordLength : undefined"
        :maxlength="password ? maxPasswordLength : undefined"
        @blur="setDirty"
      />

      <div
        v-if="inError"
        class="absolute inset-y-0 right-0 pr-3 flex items-center pointer-events-none"
      >
        <ExclamationCircleIcon
          class="h-5 w-5 text-destructive-400"
          aria-hidden="true"
        />
      </div>
    </div>

    <p v-if="props.docLink" class="mt-2 text-xs text-action-500">
      <a :href="props.docLink" target="_blank" class="hover:underline">
        Documentation
      </a>
    </p>

    <p v-if="props.description" class="mt-2 text-xs text-neutral-300">
      {{ props.description }}
    </p>

    <SiValidation
      :value="String(inputValue)"
      :validations="validations"
      :required="required"
      :dirty="reallyDirty"
      hide-required-unless-dirty
      class="mt-2"
      @errors="setInError($event)"
    />
  </div>
</template>

<script lang="ts">
export default {
  inheritAttrs: false,
};
</script>

<script setup lang="ts">
import { ExclamationCircleIcon } from "@heroicons/vue/solid";
import { computed, PropType, ref } from "vue";
import _ from "lodash";
import { useFormSettings } from "@/composables/formSettings";
import SiValidation, {
  ErrorsArray,
  ValidatorArray,
} from "@/atoms/SiValidation.vue";

const props = defineProps({
  modelValue: { type: String },
  title: String,
  id: { type: String, required: true },
  description: String,

  placeholder: String,
  password: Boolean,
  minPasswordLength: { type: Number, default: 8 },
  maxPasswordLength: { type: Number, default: 64 },

  validations: { type: Array as PropType<ValidatorArray> },
  required: Boolean,
  alwaysValidate: Boolean,

  docLink: String,

  textArea: Boolean,
  disabled: Boolean,
  loginMode: Boolean,
});

const formSettings = useFormSettings();

const emit = defineEmits(["update:modelValue", "error", "blur"]);

const dirty = ref<boolean>(false);
const setDirty = () => {
  dirty.value = true;
  emit("blur", inputValue);
};

const reallyDirty = computed(() => {
  if (props.alwaysValidate) {
    return true;
  }
  return dirty.value;
});

const inError = ref<boolean>(false);
const setInError = (errors: ErrorsArray) => {
  let nextInError = false;
  if (errors.length == 1) {
    if (_.find(errors, (e) => e.id == "required")) {
      if (dirty.value) {
        nextInError = true;
      }
    } else {
      nextInError = true;
    }
  } else if (errors.length > 1) {
    nextInError = true;
  }
  inError.value = nextInError;
  emit("error", inError);
};

const inputValue = computed<string>({
  get() {
    return props.modelValue ?? "";
  },
  set(value) {
    emit("update:modelValue", value);
  },
});

const textBoxClasses = computed((): Record<string, boolean> => {
  const results: Record<string, boolean> = {
    "placeholder-neutral-400": true,
  };

  if (props.loginMode) {
    results["bg-shade-100"] = true;
    results["text-neutral-100"] = true;
    results["disabled:border-neutral-100"] = true;
    results["placeholder:italic"] = true;
    results["placeholder:text-xs"] = true;
  } else {
    results["bg-neutral-50"] = true;
    results["border-neutral-300"] = true;
    results["dark:border-neutral-600"] = true;
    results["dark:bg-neutral-900"] = true;
  }

  if (inError.value) {
    results["border-destructive-400"] = true;
    results["focus:ring-destructive-400"] = true;
    results["focus:border-destructive-400"] = true;
  } else {
    results["border-neutral-600"] = true;
    results["focus:ring-active-200"] = true;
    results["focus:border-active-200"] = true;
  }
  return results;
});

const titleClasses = computed((): Record<string, boolean> => {
  if (props.loginMode) {
    return {
      "text-neutral-50": true,
    };
  }
  return {};
});

const type = computed((): string => {
  if (props.password) {
    return "password";
  }
  return "text";
});

const valueChanged = (event: Event) => {
  const element = event.currentTarget as HTMLInputElement;
  emit("update:modelValue", element.value);
};
</script>
