#!/usr/bin/env python3
"""
Downloads and installs workspace packages via Pnpm.
"""
import argparse
import os
import shutil
import subprocess
import sys

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
            "--package-json",
            required=True,
            help="The package.json to install"
    )
    parser.add_argument(
            "--pnpm-lock",
            required=True,
            help="The pnpm lockfile"
    )
    parser.add_argument(
        "--pnpm-binary",
        required=True,
        help="The path to the pnpm binary or wrapper script.",
    )
    parser.add_argument(
        "node_modules_out_path",
        help="Path to output `node_modules`",
    )

    args = parser.parse_args()
    shutil.copy2(args.package_json, ".")
    shutil.copy2(args.pnpm_lock, ".")

    cmd = [args.pnpm_binary, "install", "--frozen-lockfile"]

    exit_code = subprocess.call(cmd)

    if exit_code == 0:
        src = "node_modules"

        shutil.copytree(
            src,
            os.path.join(args.node_modules_out_path, "node_modules"),
            symlinks=True,
        )

    sys.exit(exit_code)
